	            <h3 class="text-muted">El efecto Christian Grey: 5 razones por las que nos enamoramos de un hombre abusivo ficticio</h3>
	            <p>Con más de 100 millones de copias vendidas a nivel mundial, “50 sombras de Grey” se ha convertido en el libro a leer; y por las reacciones al lanzamiento del tráiler, también será la película que tendremos que ver. 
Pero la real pregunta se esconde detrás del enigmático protagonista: ¿Quién es Christian Grey? y ¿Qué hace este personaje ficticio para que mujeres poderosas e independientes fantaseen acerca de un hombre que muchos consideran agresivo y controlador?</p>
				<iframe width="100%" height="360px" src="//www.youtube.com/embed/YWN6mvhsSks?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
				<p>Aquí están las 5 razones que pueden ayudar a responder estas preguntas:</p>
				<h3>1. Escapamos de la realidad</h3>
				<p>No hay nada más increíble que escapar dentro de un libro y no salir de él hasta que termina. Más aún cuando estamos leyendo acerca de un hombre con todas las cualidades que pueden llevar a la “realidad” las más extremas de nuestras fantasías: un millonario emprendedor preocupado por la sociedad, que puede bajar la luna a tus pies si es necesario, ama cada centímetro de tu ser y además, es un dios en la cama.</p>
				<p>Pero la mejor parte del señor Grey es que no es real. El no representa ningún riesgo emocional o la posibilidad de salir herida. El simplemente es la realidad de las fantasías, esperando a complacerte siempre que decidas abrir uno de sus libros.</p>
				<h3>2. A veces nos gusta un hombre con iniciativa</h3>
				<p>Muchas mujeres están acostumbradas a cuidar de todo, todo el tiempo. Criamos los hijos, manejamos nuestros hogares y nuestros propios negocios, tanto que a veces resulta desgastante. Así que de vez en cuando es emocionante alguien que tome el control completo.</p>
				<p>La mayoría de las mujeres mataríamos por un hombre que en lugar de preguntar “No sé, ¿A dónde quieres ir a comer?” simplemente diga: “hice una reserva para los dos en este restaurante esta noche”. </p>
				<p>Ahora, toma ese mismo concepto y multiplícalo por 50 (si, por 50); ese es Christian Grey, el que verá en una realidad alterna no tengas que levantar ni un dedo.</p>
				<h3>3. El oscuro encanto de una persona posesiva</h3>
				<p>Mucha gente critica a Christian Grey por ser excesivamente protector con Anastasia, al punto de ser posesivo. Y aunque ninguna de nosotras quiere un hombre que la rastree constantemente, es excitante pensar a en un hombre que puede amar tanto como para llegar a esos extremos. </p>
				<p>Es el mismo concepto detrás de una pelea en un bar. Dos hombres dándose golpes por un altercado durante una noche de copas es simplemente ridículo, pero, la idea de que tu hombre se pueda molestar tanto por el hecho de que alguien más te miré, te puede llenar de los sentimientos más pasionales aunque no sea saludable. </p>
				<img src="../_img_articulos/anastasia_md.jpg"/>
				<h3>4. Él rompe los tabúes</h3>
				<p>“50 sombras” fue el primer libro erótico de muchas mujeres. Y aunque para muchos expertos en el tema los libros estaban llenos de errores e imprecisiones, Christian Grey introdujo a las mentes más inexpertas en una odisea sexual.</p>
				<p>En un mundo lleno de encuentros sexuales poco complacientes, la posición del misionero y como la autora de los libros E.L. James lo llama “Sexo vainilla”, es muy sensual tener la posibilidad de explorar áreas nuevas y llenas de tabúes de la sexualidad a la que normalmente no nos vemos expuestos.</p>
				<p>Y aunque no tenga mucho sentido que las mujeres fantaseemos acerca de ser atadas, dominadas y dictadas, si resulta muy sensual pensar entrar en un cuarto rojo de placer para ser sometida por el excéntrico Christian Grey, siendo esta idea la premisa de un libro y no una habitación real en nuestros hogares. </p>
				<h3>5. Si ellos están rotos, nosotras los reparamos</h3>
				<p>A algunas mujeres nos gusta rehabilitar nuestras parejas y nos han enseñado a ser así. Vemos a un hombre y buscamos la oportunidad para completarlo. </p>
				<p>Para Christian, su defecto se encuentra en su turbio pasado, y la única cosa más sensual que un magnate millonario con un síndrome dominante es un magnate millonario que también puede ser vulnerable y dependiente de tu consuelo.</p>
				<p>La superioridad física y la delicadeza emocional hacen una perfecta combinación, de esas que rara vez se ven en la vida real. Así que tiene sentido que la mayoría de nuestros héroes tengan trágicos pasados, ese solo hecho nos permite generar simpatía y nos ayuda a acercarnos a ellos.</p>
								<p>Y si todo esto no te da suficientes motivos para idolatrar a Christian, para muchas de nosotras él es tan perfecto como puede ser. Si quieres recibir más artículos como este sobre educación sexual moderna de calidad, suscríbete a nuestra página.</p>

