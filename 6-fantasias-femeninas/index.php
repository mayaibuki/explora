					<p>A continuación las 6 fantasías más comunes en las mujeres:</p>
			
					<h3>Sentirse amadas durante el sexo</h3>
					<p>Para sorpresa de pocos, el tener sexo con una persona que las hace sentir amadas es la fantasía más común. También es la fantasía más popular entre los hombres. Después de todo, tener sexo con una persona que te ama, te aprecia y te respeta sinceramente es increíblemente sexy.</p>
			
					<h3>Acostarse con otra mujer</h3>
					<p>Muchas mujeres fantasean con acostarse con otra mujer, con alguien que sepa por experiencia propia qué lugares tocar, cuándo tocarlos y cómo tocarlos. Si tienes esta fantasía, no necesariamente significa que seas lesbiana, sólo significa que encuentras el cuerpo femenino erótico.</p>
		
					<h3>Disfrazarse o "Role-play"</h3>
					<p>Disfrazarse y actuar con tu pareja es una fantasía muy sana, permite experimentar y romper la rutina en un entorno seguro. Los disfraces más comunes suelen ser de profesor y estudiante, policía y criminal, paciente y enfermera, sirvienta o mayor domo, prisionero, azafata, etc.</p>
		
					<h3>Dominatrix</h3>
					<p>Una dominatrix, suele ser una mujer que domina a su pareja en ropa muy sexy, latex, cuero, cadenas, vestida muy parecida a gatubela. Esta es una fantasía muy común, pues combina el sentirte deseada, que te rueguen por más y estar en una posición de poder y control absoluto.</p>
		
					<h3>Ser acosado sexualmente</h3>
					<p>Aunque suene loco, esta es una de las fantasías más comunes, se fantasea que alguien no puede controlar todos los deseos sexuales que se siente hacia ella y la obligan a tener sexo en contra de su voluntad, ella puede protestar todo el tiempo, pedir ayuda y al mismo tiempo, en secreto, disfrutar cada segundo.</p>
					
					<p>Esta fantasía tiene a mucha gente confundida, ¿Cómo es posible que alguien fantasee ser violado? Lo que el subconsciente dice, no es lo que la persona realmente desea. <b>Ninguna persona quiere ser violada</b> y todo acto sexual debe ser consensual, <b>sin excepción</b>.</p>
					<p>No se sabe muy bien cuál es la razón por la cuál esta fantasía sea tan frecuente, pero hay una hipótesis que dice que debido a que las mujeres sienten mucha presión social y se les avergüenza por tener sexo; al ser violadas, ellas no son culpables del acto sexual y no se sienten culpables de sentir placer al tener sexo.</p>
		
