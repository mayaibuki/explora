					<h1>Problemáticas de sexualidad en Colombia</h1>
					<h5 class="text-muted">Por Jorge Maya, 3 de Julio 2015</h5>
	
		            <p>Existe una jerarquía invisible que discrimina, humilla, lastima, empobrece y asesina a diario a algunos hombres y a muchísimas mujeres.</p>

<p>Explora.life quiere erradicar la desigualdad y la falta de libertad sexual que afectan tanto hombres como mujeres. Para esto trabajamos para resolver estos problemas:</p>

<h3>La sociedad privilegia a los hombres</h3>
<p>Vivimos en una sociedad que privilegia sólo a los hombres que cumplen el estándar de ser un hombre y adopta a las mujeres que siguen al pie de la letra las reglas creadas por estos hombres de lo que debe ser una mujer.</p>

<p>Las personas que no sean hombres heterosexuales, blancos, ricos, fuertes, violentos y con características masculinas son oprimidas sistemáticamente. Es decir, que la sociedad los oprime al no brindar la misma libertad, derechos, oportunidades laborales, protección, ni prestigio.</p>

<h3>Existe menosprecio a lo femenino</h3>
<p>En una sociedad como la colombiana, lo peor que puede llegar a ser un hombre, es ser una mujer. Ser femenino es visto como signo de debilidad pues tanto hombres como mujeres piensan que los hombres son superiores, las mujeres y los hombres con características femeninas son inferiores antes los ojos de la sociedad.</p>

<p>La cotidianidad colombiana se burla constantemente, desprecia lo femenino y las personas que no son o no cumplen con el estándar de lo que significa ser un hombre, eso se llama machismo.</p>

<h3>Las mujeres son consideradas inferiores</h3>
<p>Se piensa que las mujer son buenas para criar hijos, atender personas, mantener la casa o trabajos que requieren un sentido estético como el diseño de moda o cortar el pelo. Pero no se confia en sus capacidades para tomar decisiones, administrar dinero, matemáticas o ningún trabajo que requiera habilidades intelectuales.</p>

<p>Por lo tanto no suelen obtener trabajos bien pagados; lo que las obliga a depender económicamente de sus parejas, usar su atracción sexual para obtener beneficios laborales o vender su cuerpo.</p>

<h3>Deben usar su sexualidad para sobrevivir</h3>
<p>La mujer tiene la responsabilidad de ser sexy y verse bonita, en todo momento. Se le exige que tenga el cuerpo que los hombres consideran hermoso, sin importar lo que a ella le gusta o quiera.</p>

<p>Al mismo tiempo se le hace sentir culpable, inferior o es humillada públicamente por verse demasiado sexy, demasiado atractivo por mostrar interés en disfrutar del sexo.</p>

<p>Esto es un problema grave porque obliga a las mujeres a tener un estilo de vida que no todas quieren. Son obligadas indirectamente a maquillarse, vestirse de cierta forma, estar a dieta, ir al gimnasio, depilarse o tener cirugías plásticas, incluso si ellas no quieren. Al mismo tiempo se les prohíbe coquetear, vestirse de forma provocativa, tener sexo fuera del matrimonio, sentir placer al tener sexo, etc. Así se les culpa si son acosadas sexualmente o violadas, se culpa a la víctima por haber sido coqueta, no al perpetrador por violar la intimidad y cuerpo de otra persona.</p>

<p>Se les exige que usen su sexualidad para encajar en la sociedad pero se les castiga si usan su sexualidad para ellas mismas.</p>

<h3>La mujer como un objeto de placer</h3>
<p>Se piensa de forma equivocada que la mujer está para complacer al hombre. Se cree que las mujeres aportan a la sociedad su belleza y lo único valioso que tienen para ofrecer es placer.</p>

<p>Valorar a las mujeres sólo por el placer que pueden dar, esto les impide explotar las capacidades que tienen y lograr lo que se proponen. Las mujeres son valiosas por ser seres humanos y pueden aportar a la sociedad su inteligencia, sus habilidades, sus opiniones, su humor al igual que un hombre.</p>

<img src="../media/articles/ciclo-vicioso.jpg" alt="ciclo-vicioso" width="100%" height="" />
				<p class="small text-secondary text-right">
					Ciclo vicioso del sexismo
				</p>
<p>Es un ciclo vicioso en donde la sociedad privilegia a los hombres, creando un miedo por lo femenino, que hace que las mujeres sean tratadas como algo inferior, lo cual las pone en desventaja y las obliga a usar su sexualidad para mejorar su calidad de vida, lo que hace que sean sexualisadas y tratadas como un objeto de placer, creando una sociedad que privilegia a los hombres.</p>

<h2>¿Cómo queremos solucionar esta problemática?</h2>

<p>La ignorancia es la raíz del problema. Nuestro objetivo es que las personas aprendan a identificar actitudes machistas, a cuestionar los valores tradicionales y a aceptar que llevamos 5 milenios viviendo en sociedades patriarcales que subyugan a la mayoría de sus miembros.</p>

<p class="large text-secondary">Creemos que la educación, el acceso a la <a href="/leer/">información</a> y a <a href="/comprar/">experiencias nuevas</a> es el arma más poderosa para apropiarte de tu cuerpo y tu sexualidad.</p>
<p>Explora.life brinda acceso, en español, a educación sexual moderna, al día, que celebre el sexo como una experiencia positiva en nuestras vidas en vez de un acto vergonzoso, primitivo o pecaminoso. Proveemos las herramientas necesarias para que la sociedad Colombiana sea más libre, sin prejuicios, más equitativa y ofrecemos experiencias seguras para disfrutar y explorar tu sexualidad de una manera sana y divertida, que mejore tu calidad de vida.</p>

