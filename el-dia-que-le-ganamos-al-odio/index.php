					<h1>El día que le ganamos al odio</h1>
					<h5 class="text-secondary">Por Ramona González, <a href="https://twitter.com/ramonaquegrita" target="_blank">@ramonaquegrita</a></h5>
					<h5 class="text-muted">Julio 26, 2015</h5>
	
<h3 class="section-parrafo">La aprobación del matrimonio entre parejas del mismo sexo le devolvió al mundo un poquito de esperanza, de fe en la vida.</h3>

<p>El día que le ganamos al odio todos nos vestimos de arcoiris. La victoria fue en Estados Unidos, es cierto. Pero aquí, en Colombia, al otro lado de ‘América’, <b>volvimos a creer en la gente, en la justicia</b> y en la pasión por la vida.</p>

<p>El día que le ganamos al odio recuperamos la esperanza. Entendimos que <b>somos realmente iguales ante la ley</b>, sentimos que nuestra Corte Constitucional, inspirada en los jueces estadounidenses, le hará justicia al ideal de equidad que cimentó la Constitución de 1991. Que, siguiendo a Barack Obama la Corte entenderá que <b>"sobre todas las diferencias, somos todos iguales”</b> y que el “amor es el amor” y que si en Estados Unidos no hay “ciudadanos de segunda”, aquí tampoco.</p>

<p>El día que le ganamos al odio confirmamos que a punta de besos, abrazos y colores, podemos superar la discriminación.</p>

<p>El día que le ganamos al odio recordamos el informe que la ONG ‘Colombia Diversa’ presentó sobre el sufrimiento de la población LGBT en las cárceles del país. “Tienen que enfrentarse a la violencia por parte de sus compañeros de reclusión y al rechazo de las autoridades. Sin contar que se les niega, usualmente, su derecho a la visita íntima”, denunció el periódico El Espectador basado en el informe. Dijimos que esto debe cambiar, que <b>como sociedad no podemos permitirlo</b>, debemos cuestionarlo, gritarlo, insistir en la transformación. </p>

