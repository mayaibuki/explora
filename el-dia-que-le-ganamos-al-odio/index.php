<p>El día que le ganamos al odio, el 26 de junio de 2015, pasará a la historia de la misma manera que el 7 de julio de 1954, cuando entró en vigencia la Convención sobre los <b>derechos políticos de la mujer</b>, que hizo posible el sufragio femenino. Pasará a la historia de la misma manera en que lo hizo el movimiento por los derechos civiles en Estados Unidos y se quedará por siempre en nuestra memoria. </p>

<p>El día que le ganamos al odio no importó nada más. Si bien supimos que ahí no terminaba la lucha, que <b>la discriminación contra las personas LGBT no cesa ni Estados Unidos ni en el resto del mundo</b>, celebramos. Celebramos porque la ley es uno de los escenarios más importantes de esta arremetida por la igualdad, aunque no el único.</p>

<p>El día que le ganamos al odio, ignoramos el escepticismo. Que las grandes compañías aprovecharon el momento para vender más y llenarse de dinero. Que es una estrategia electoral de la Casa Blanca, que aquí no hay buenas intenciones, que todo tiene un interés político y económico. Lo ignoramos, porque le ganamos al odio con amor y un triunfo semejante, en una sociedad donde prima la competencia y la ley del más fuerte, alivia nuestra existencia. </p>

<p>El día que le ganamos al odio en Estados Unidos le ganamos al odio en el mundo: <b>el poder de lo simbólico es inconmensurable</b>. </p>
