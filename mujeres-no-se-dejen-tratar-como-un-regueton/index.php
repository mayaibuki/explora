					<h1>Mujeres, no se dejen tratar como un reguetón</h1>
					<h5 class="text-secondary">Por Anónimo, 8 de Julio 2015</h5>
	
<p>La mayoría de canciones de este tipo de música tiende a denigrar el género femenino colocándolo a las mujeres, como esclavas y objetos sexuales. </p>

<p>Pocos días antes de morir, <a href="https://www.facebook.com/florezair" target="_blank">Jhonathan Flórez</a>, el bien llamado ‘Hombre Pájaro’, arremetió en sus redes sociales en contra del reguetón. El deportista extremo, dueño de cuatro récords mundiales en la modalidad de Salto Base, subió un video donde se le mostraba indignado y sorprendido de las letras que este género musical tiene en la mayoría de sus canciones. </p>

