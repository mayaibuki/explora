<p>Flórez, que falleció el pasado 4 de julio de 2015 en Suiza mientras se encontraba practicando para representar a Colombia en el Mundial de su modalidad en China, el 9 de junio de este año, se dio a la tarea de subir 14 letras de canciones de diferentes artistas de reguetón donde el género femenino es degradado, maltratado y prima una actitud machista. El ‘Hombre Pájaro’ no alcanzó a llegar a China, pero su mensaje por un mejor trato a las mujeres, fue entregado. </p>

<p>Y en esta pequeña muestra que este gran deportista colombiano dejó en su muro de Facebook, se puede concluir algo: A las mujeres desde esta “cultura urbana” se les ve como un objeto sexual, se les trata como esclavas sexuales y se les muestra como unas cualquieras, interesadas y regaladas. </p>


<p>Lo triste del caso es que esta aberrada visión no está muy lejos de la lamentable percepción que se crea en nuestro entorno y que con productos como estos, se fortalece y se llega a convertir en una falacia que todos terminan creyendo como cierta. </p>

<p>Ese concepto errado, donde se ven mujeres en ropas menores, haciendo lo que los "magnates" les ordenan. Montadas en carros lujosos, habitando extravagantes y a veces, muchas veces, casas de muy mal gusto es lo que tiene jodida a esta sociedad. No puede ser que una mujer siempre tenga que ser el adorno de una fiesta. No puede ser que siempre se tengan que mostrar el mismo tipo de mujer, con el mismo estilo, y la misma manera de vestir, como si quisieran hacernos creer que existe una fábrica de donde todas salen igual, solo que con año de modelo distinto. Basta, en serio basta.</p>

<p>Con estas ideas crecen los chicos y con estas ideas crecen las chicas. Con estas ideas se quedan personajes como Donald Trump, y comediantes como Belén Mora en Chile, y en todas partes del mundo donde relacionan a las latinas con la prostitución y así las tratan. </p>

<p>Ese exhibicionismo incrusta en lo profundo de la mente dos doctrinas: 1. Que las mujeres son unas cualquiera y hay que tratarlas como tal. 2. las mujeres piensan que son unas cualquiera y se dejan tratar como tal.</p>

<p>No se dejen tratar como un reguetón. Sean un reguetón, rico que lo sean, sin pena ni contemplaciones, seguras de sí mismas, dueñas de sus decisiones y elecciones, pero no se dejen tratar así.</p>

<p>Les explico: los hombres que llevan el ritmo en esas canciones pegajosas que desde hace más de una década lideran todos los listados de música latina, dan órdenes en sus canciones. En su mal hablado español y sus letras sin sentido, uno alcanza a comprender lo que ellos quieren decir. </p>

<p class="text-secondary"><i>"Ándate sucia que hoy quiero esto, siéntate tócate el pelo, hazme el amor y ahora desaparece y espera a cuando te vuelva a llamar. Es tan regalada que me llama siempre a las tres de la mañana, le doy a ella y le doy a su hermana, a su mamá también, al final son una familia de zánganas. Hago lo que quiero y lo mejor, es que ninguna me reclama, siempre está esperando por mí y la contento sacándola a pasear en mi carro, regalándole dinero y montándola en yates. Las mujeres, sí, están hechas para esto y así vamo’ a seguir, toda la noche, toda la semana, toda la vida. Yo ordenando y ellas complaciendo.” </i></p>

<p>Ustedes pueden ser un reguetón, sí. Comportarse como una diabla que se toca sola y sin vergüenza, que enloquece cuando le pasan las manos por la espalda. La que pide que no dejen de satisfacerla porque la hacen feliz, la que complementa la fiesta en la cama con objetos de placer, la que le gustan los olores, los sabores, los aceites. La que se viste por ella y para ella. La que explora cada vez más porque sabe que el placer tiene un apetito infinito y no le da miedo experimentar, ni le hace caso a los prejuicios. La que sabe que es una persona responsable y dueña de su vida. </p>

<p>Pero, ¿por qué alguien va a venir a gritarlo a los cuatro vientos y a hacerlas ver como si estuvieran obligadas siempre a hacerlo? ¿Por qué alguien va a venir a relacionarlas como un instrumento de mercancía para que después de hacer las cosas que les gusta hacer se sientan indecorosas, sin moral, mal con ustedes mismas, u obligadas? </p>

<p>No, no es así. El hecho de que a ustedes les guste el sexo, como a cualquier persona en la vida, no las condiciona ni las clasifica, y por ende, no les obliga a nada. Que se acabe esa idea idiota, sumisa y mojigata por el amor de Dios. </p>

<p>No más con esos espectros que van el vida creyéndose ‘reguetoneros’ y tratándolas mal, hablando mal de ustedes, usándolas y manipulándolas. Digo no más porque la solución la tienen ustedes con sus actitudes, porque la gente idiota no deja de nacer y si no nacen, el entorno la convierte. Entonces depende es de ustedes generar el cambio. </p>

<p>Ustedes pueden poner en su sitio a estos pelmazos ignorando esos comentarios, esos insultos que pretenden dominarlas y hacerlas sentir culpables y llenas de miedo para después controlarlas, porque no hay motor más fuerte que el miedo y la culpa. </p>

<p>Estos tipos son los que se tiran todo, la confianza en ustedes mismas y por ende, se le cagan la vuelta a los que tipos que vienen, que posiblemente no van a ser un reguetón, sino una sinfonía de Beethoven, y se van a privar de lo que realmente son ustedes, porque ustedes van a estar pensando que lo que hicieron antes, estuvo mal y no van a querer mostrarse y liberarse como son.</p>

<p>Sean un reguetón siempre que se les venga en gana, sean un reguetón con el que quieran (eso sí, no sobra decir que sin lastimar a nadie); pero no anden con uno que las trata como las trata el reguetón.</p>

<p>Júntese con tipos que las traten como otros géneros, que entiendan ese dicho de "dama en la calle, loca en la cama". Que entiendan ese dicho del " hombre propone y la mujer dispone" que sepan tocar al menos un instrumento y no sólo recitar bobadas que riman pero que muchas veces no tienen sentido.</p>

<p>El empotramiento de las actitudes es el inicio de los cambios. Tal vez los pendejos que se creen con el poder de ir a tratar como se les da la gana a las mujeres, al ver que su estrategia de terror ya no funciona, comiencen a analizar y a darse cuenta de que no viven en un vídeo de reguetón.</p>

<p>El reguetón es bueno para bailar, uno la pasa rico. Pero no crean todo lo que estos “artistas” dicen ahí. Andar con esa música para arriba y para abajo en la cabeza, no es buena idea. </p>

