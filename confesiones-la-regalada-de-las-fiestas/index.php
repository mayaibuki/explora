<h1>Capítulo 1:</h1>
<h3>De cómo evitar ser la “regalada” de las fiestas</h3> 

<h3 class="section-parrafo">Esto se llama <i>“de cómo evitar ser la ‘regalada’ de las fiestas”</i>. En realidad, “regalada” es el eufemismo que me toca poner en el título para que no me censure Facebook. A decir verdad esto se llama, <i>“de cómo evitar ser la “puta” de las fiestas”</i>.</h3>

<p>Me pasa todo el tiempo que quiero rumbearme con más de un man en una sola noche. <b>Pero en nuestra cultura eso está muy mal visto</b>. Incluso, mis amigas, aunque son seres maravillosos que me aman profundamente, me mirarían mal si me ven saltando de boca en boca. Si me pongo a pensar en lo que diría mi mamá, también me vería mal a mí misma. Lo que hago, simplemente, es bloquear a mi mamá de mis pensamientos. Pienso en mí, en lo que quiero, en lo rico que me va a resultar sentir, sentir de todo, dar muchos primeros besos en poco tiempo.</p>

<p>Pero <b>tampoco me gusta cargar con el rótulo de “puta” tatuado en las tetas</b>.  Nadie se aguanta eso, nadie quiere eso en Colombia. Yo sé que no soy una puta aunque nadie más parezca saberlo. Me pasó el sábado. Estaba en el cumpleaños de un amigo de una amiga. No los conocía, a nadie, llegué ahí porque una amiga me pidió que la acompañara. Sin embargo, era gente de la Universidad y qué mamera los chismes.</p>

<h3>Por eso era el momento perfecto para armarme un teatro:</h3>

<p>-Mi novio me puso los cachos, hace un mes. Estoy muy mal y muy triste-</p>

<p>Empecé a fingir que bebía, <b>pero no bebía nada</b>. Cada vez que podía me iba corriendo a tirar la cerveza en el baño. No me gusta tomar. No es algo que juzgue en otros, es que a mí me gusta más mi conciencia.</h3>

<p>Y con eso me di permiso. <b>Me besé con dos tipos</b>, aunque no se me dio la gana de irme a la cama con <b>ninguno</b>. Hay una cuestión que no tiene que ver con qué tan libre soy, sino con qué tanta <b>confianza</b> me genera la gente con la que ando. Uno de los manes se puso muy bravo, me dijo: “me hubieras dicho que estaba perdiendo mi tiempo”, cuando le respondí que no me iba a su apartamento.</p>

<p>¿Qué tiene en la cabeza un tipo que cree que dar besos en el cuello y en la boca es perder el tiempo? <b>¿Y desde cuándo responder a esos besos significa que estoy obligada a tirar?</b></p>

<p>Claro, en un mundo ideal, no debí haber mentido. No debí haber fingido estar borracha para tener permiso de rumbearme con dos manes y <b>no ser juzgada al día siguiente</b>.</p>

