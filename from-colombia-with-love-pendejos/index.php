					<h1>From Colombia with love, pendejos</h1>
					<h5 class="text-muted">Por Ramona González (<a href="https://twitter.com/ramonaquegrita" target="_blank">@RamonaQueGrita</a>), 5 de Julio 2015</h5>
	
<p>Donald Trump en Estados Unidos y la actriz María Belén Mora en Chile, se encargaron de recordarnos por estos días que seguimos siendo violadores, narcotraficantes y putas en el resto del mundo. Aquí me voy a quedar con lo de “putas”, que es lo más entretenido. De los violadores y narcotraficantes hablarán personas más cultas.</p>

<iframe width="100%" height="360px" src="//www.youtube.com/embed/Xl8r3K-jn3s?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>


<p>No somos putas, ojalá, señor Trump, ojalá señora Mora. Ojalá las mujeres en Colombia y en otros países de América Latina pudiéramos tener determinación sobre nuestro cuerpo, tomar nuestras propias decisiones sin miedo al marido y encontrar la libertad y equidad establecida para todos los ciudadanos en la Constitución. Ojalá pudiéramos caber en esa palabra, “puta”, porque aquí vamos llamando “putas” a las que tomamos anticonceptivos, nos divorciamos, nos masturbamos y tenemos sexo con quien queramos y no con quien nos toca. </p>

<p>No somos putas, pero sí somos otras cosas: somos 15.640 víctimas de violencia intrafamiliar solo en el primer semestre de 2013, según cifras de Medicina Legal. Somos las 5.545 que denunciamos ser víctimas de abuso sexual ese mismo año. Somos las 17 quemadas con ácido en 2011, cifra que nos pone justo debajo de Bangladesh y Pakistán, los dos países en el planeta con más crímenes de este tipo. Somos Natalia Ponce de León, quien se convirtió en la imagen de esta nueva forma de feminicidio, cuando decidió ignorar a un tipo pues simplemente no le gustaba y por eso él vino a quemarle el cuerpo y matarla en vida. Somos ella y todas las que esperan justicia. Somos Jineth Bedoya, periodista violada por paramilitares como venganza por una investigación. Somos Jineth, a quien hoy, dice, la violaron de nuevo porque a su victimario lo dejaron libre y anda en la calle. </p>

<p>Somos, también, esclavas de nuestro cuerpo, esclavas de la prostitución en un país que nos pone en cuatro, sin preguntarnos si queremos. Somos la niña de 11 años en Cartagena, vendiéndose a los turistas, muchos de ellos, señor Trump, bien gringos del promedio. Somos la niña de 13 años que le cuenta a su maestra en Cúcuta que su mamá permite que un señor vaya a la casa a tener sexo con ella y así mantiene la casa. Somos las prostitutas de la Primero de Mayo en Bogotá, tirando por plata en las peores condiciones de salubridad y sin dignidad alguna. Somos las prostitutas de Antofagasta en Chile, sí esas somos nosotros. Y ustedes, señora Mora, los que nos pagan.</p>

<p>Somos las niñas que soñamos con ser Miss Universo, coronadas por usted, señor Trump. Somos las niñas que desfilamos en traje de baño en el desfile ‘Miss Tanguita’ en Barbosa Santander, convirtiendo desde los cinco años el cuerpo en nuestro principal “recurso natural”. En nuestro único recurso, porque este país no nos ofrece posibilidades para cultivarnos como bailarinas, escritoras, ingenieras y, cuidadito, cuidadito, futbolistas. Somos las niñas en las ciudades, en las casas que tienen televisión por cable, consumiendo adictas MTV y aprendiendo cómo comportarnos para gustarle a los chicos. Somos las madres de familia que miramos Discovery y nos antojamos de que nuestras chiquitas, igual que las de ustedes, se metan a cámaras de bronceo, se pongan dientes postizos y hagan abdominales desde los cuatro años para convertirse en ‘Miss Teen USA’. Somos Paulina Vega, señor Trump, su reina, nuestra segunda Miss Universo de la historia. La que exhibe en cientos de eventos sociales, la que pone su cara bella para conseguirle acuerdos y fortalecer su imagen en el mundo. </p>

