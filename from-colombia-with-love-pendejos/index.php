<p>Somos todo lo que aquí he anotado. Y nos duele profundamente serlo. Pero eso sí, no somos los hipócritas que coronamos a Paulina y luego defendemos una ley anti-inmigrantes. No somos los hipócritas que hacemos tratados de libre comercio con América Latina, no somos los hipócritas que basamos nuestro ideal de país en los inmigrantes y luego hacemos campaña a punta de un discurso xenófobo y racista.</p>

<p>Claro que nos tenemos que ofender con usted señor Trump y también con su broma, señora Mora. Nadie tiene por qué ir a matarlos por eso, ni a amenazarlos, ni a agredirlos con violencia, porque somos mucho más que eso. Pero sí tenemos derecho a gritar que nos duele lo que dicen y a pedirle a Paulina que renuncie, porque nos hiere que vengan a simplificar de esa forma un sufrimiento que llevamos tatuado en la piel y que nos sigue haciendo daño. Tenemos derecho de manifestarnos contra ustedes si, además, van a Antofagasta a tirar con nosotros o si tienen tanta plata invertida por estas tierras. Hipócritas. </p>

<p>No somos putas, ojalá. Somos esta manada de mujeres ofendidas con ustedes y por eso les escribimos esta carta, desde Colombia con amor. Pendejos.</p>
