<h3 class="section-parrafo">No voy a escribir aquí una serie de pasos que usted debe seguir al pie de la letra, porque no creo en ese tipo de cosas. Solo voy a hablar de mi experiencia y, espero, eso será más útil.</h3>
<p>Hace unos minutos vi una foto en plano general de mi misma, pero más que un autorretrato mi cámara creó conciencia. Me senté frente a esa imagen y sonreí, estaba viendo un cuerpo con el que me sentía <b>completamente satisfecha</b>. “Vanidad, vanidad, vanidad”, pensé en un primer momento, luego abrí los ojos para encontrarme con que los defectos que algún día me acomplejaron. Seguían ahí, incluso aquellos que escapan al lente. Ahí estaban, aquí están, sentados conmigo, escribiendo esta declaración de amor propio. Sin embargo, han dejado de importar. Me encontré de nuevo, llegué a un punto de tranquilidad, <b>me siento conforme con lo que veo y con lo que soy</b>.</p>

<p>Entonces recordé cada vez que mi pareja exaltó las marquitas en mi piel, cada centímetro con celulitis, cada vejiga en mis dedos, cada dureza en mis talones. Recordé su recurrente costumbre de apretar mi piel en busca de ello, <b>su gesto despectivo</b> y el disfraz del circo que estaba montando: me encanta, no tienes de que preocuparte… o algo así. No sé cuántas veces pasó, ni que tan rápido penetró en mi vida, pero luego de casi 3 años de relación, <b>no quería tener sexo jamás</b> <b>y mi cuerpo me parecía un monstruo</b>. Me llené de terror, de imperfección, de necesidad. No supe que eso pasaba, nunca lo pensé así, pero ahora me parece el primer paso para establecer control. </p>

<p>Él era una persona posesiva, establecía su poder de forma evidente y ahora sé que también lo hacía <b>con trucos al autoestima</b>, con miras a la debilidad. Lo logró. Lloré unas cuantas veces, no por mi cuerpo, pero sí por la idea que recorría mi mente. Dejé de utilizar ropa corta, faldas, vestidos, que, por cierto, incitaban un <b><i>“solo se lo pone cuando esté conmigo”</i></b>. Dejé de saludar a la gente, de mirar a los ojos, de bromear en la calle. Pero nada de eso importa cuando pienso en qué dejé de sonreír al sentarme frente a mi ser. Sin embargo, ante sus ojos era una rebelde sin causa que luchaba estúpidamente. </p>

<p>Su siguiente paso importante fue atribuirme una personalidad agresiva, para ocultar la sumisión que yo cargaba encima, para evitar que lo descubriera. Cuando acabó, por gracia de Dios y por intervención de otros amores, <b>los amores de quienes me conocieron libre</b>, de quienes luego me vieron sometida a un régimen de costumbres inútiles que no me hacían feliz; cuando acabó no pudo dejarme ir. Aún no lo acepta y creo que no lo hará nunca. Pero yo superé la culpa, la adrenalina frente a la nueva libertad y volví a encontrarme, así que no daría ni un paso atrás. Excepto hoy, para reconocer que <b>la manipulación y la violencia emocional son enemigos invisibles</b> para quien los vive. Ahora sé que solo desde afuera las cosas se ponen por completo claras, pero hay que reconocer señales y con todo esto quiero llegar a recordarme y recomendar a quien pueda interesar, que cuando se piense en no contar ciertos detalles a su confidente –sea quien fuere- porque desde afuera podría verse mal y así, proteger a esa persona de la desconfianza de los demás, realmente, algo malo está pasando.</p>

