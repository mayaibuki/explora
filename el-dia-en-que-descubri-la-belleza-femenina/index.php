					<h1>El día en que descubrí la belleza femenina</h1>
					<h5 class="text-muted">Anónimo, 6 de Julio 2015</h5>
	

<p>Comenzaré por decir que soy mujer, una joven común y corriente que come, lee, no hace ejercicio y se alimenta sin contar las calorías que consume cada día. Nací en una familia extremadamente heterosexual y crecí con dos hermanos que parecen seguir el mismo esquema. Cuando era más joven de lo que soy ahora, en vez de comer hombres me dedicaba a comer libros. Frente a mis amigos era una chica más en medio de la multitud errante que compone nuestra existencia. </p>

<p>Cierto día -lo relataré como un cuento para que sea más ameno- sentí esas mariposas en el estómago que, se supone, las chicas deben sentir cuando ven pasar al tipo más guapo del barrio. Una descarga de éxtasis me recorrió el cuerpo y mi corazón latía tan rápido que por poco se me sale del pecho. ¿Qué sentía? ¿Qué significaba esa sensación de adrenalina intensa? ¿Por qué tartamudeaba sin poder proferir palabra alguna?</p>

<p>Lo que había visto me gustaba y me llenaba de alegría; esa llama de pasión de la pubertad se encendía por fin y me quemaba la garganta. Las ganas de gritar fueron ahogadas por una timidez inesperada y no tuve más remedio que quedarme allí parada reflexionando al respecto. En ese momento mi cabeza daba vueltas y, aunque estaba feliz, experimentaba cierta sensación de "terror" cuando recordaba a la persona que me había desestabilizado tan radicalmente.</p>

<p>Era una chica, como yo. Tenía dos brazos y dos piernas, dos ojos, una nariz justo en medio de la cara, dos orejas por las que, si no es poca mi inteligencia, debía escuchar como todos los demás. Su rostro era perfecto y su cuerpo simétrico… de muerte. Me gustaba, sí, me encantaba... ¡Pero era una niña!</p>

<p>¿Qué sería de mí ahora? ¿Acaso debía exiliarme para que el mundo no tuviera que pasar la pena de contemplar semejante aberración? Vaya lío en el que me encontraba, y aunque pensé que saldría algún día de tan extraña situación, los años me condujeron más y más al exquisito mundo de lo que llamaré, por razones de diplomacia, la "homoafectividad".</p>

<p>Así comencé a vivir todo tipo de experiencias. Después de explorar la totalidad de mi cuerpo me dediqué a conocer los recovecos de unas cuantas chicas, y aunque en ocasiones era similar a pararme frente a un espejo, cada mujer era distinta. Me familiaricé tanto con aquél mundo vetado, conocí a tantas personas como yo, me relacioné con opciones de vida tan "excéntricas", que logré comprender por fin lo que significa la diversidad humana.</p>

<p>El ser humano es tan diverso que establecer parámetros de comportamiento puede resultar, en ciertos contextos, un ejercicio completamente ridículo. Dar por hecho que porque ser mujer debe existir algún "galán" para quien estar "bonita", o que porque se es hombre se debe invitar a salir a determinada chica, es establecer de antemano que todas las personas somos iguales. No, no todos somos iguales, no todos soñamos con las mismas cosas ni aspiramos a los mismos objetivos. No a todos nos gusta vestir igual ni actuar de la misma manera, los roles de género son construcciones sociales cuya línea divisoria se va desvaneciendo a medida que la sociedad madura y comprende que la diversidad como la diferencia, en lugar de ser una gran desgracia, son lo mejor que le ha podido pasar a la humanidad. La subjetividad es lo más bonito que tenemos como seres humanos y es un tesoro que debemos proteger. </p>

<p>A lo mejor el título de éste texto confunda a algunos lectores, "el día en que descubrí la belleza femenina" parece tirar la balanza hacia el lado de las mujeres, pero esa no es la verdadera intención. Lo que pretendo lograr con todo esto es expresar abiertamente que soy chica y que me gustan las chicas, que eso no está mal, que me siento completa y única cuando contemplo la belleza femenina, y que aunque tengo la certeza de que los hombres poseen una belleza infinita, hoy puedo levantar la mano y decir sin preocupaciones: "me gustan las mujeres".</p>

<p>El día en que descubrí la belleza femenina, una puerta que había estado cerrada durante mucho tiempo se abrió ante mí y me permitió ver lo que muchos no tienen la dicha de apreciar. Siempre había estado tan encerrada, tan ensimismada, tan introspectiva, que poco me había detenido a pensar en aquello que mi cuerpo deseaba con tantas ansias pero que mi cabeza, cegada por los prejuicios de esta sociedad mezquina, nunca había podido aceptar. </p>

<p>Hoy puedo decir con certeza que me siento libre para amar y que no tengo más ataduras que las que mi carácter, si llegara a flaquear en algún instante, me puede llegar a imponer. No me doblego ante ninguna circunstancia ni pongo en duda la naturalidad de mi opción de vida, no cuestiono aquello que hace parte de mi ser y que, sin alardear, me hace sentir sumamente interesante. </p>



