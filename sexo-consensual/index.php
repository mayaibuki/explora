	            <h3 class="text-muted">Al empezar cualquier tipo de encuentro sexual es muy importante tener el consentimiento de tu pareja</h3> 
	            <p>Existen señales físicas y verbales que puedes usar tú o tu pareja en caso de querer pedir algo que te gusta, querer bajar el ritmo o detenerte. Cuando hay alcohol y drogas involucradas es imposible hablar de un consentimiento, aquí tenemos algunos puntos fundamentales a la hora de reconocer el consentimiento real de la pareja:</p>
				<h3>¿Qué es el consentimiento sexual?</h3>
				<p>El sexo con consentimiento es cuando todas las personas involucradas en el acto quieren participar. Si todos expresan un claro “Si”, entonces hay consentimiento.</p>
				<h3>¿Por qué es importante el consentimiento?</h3>
				<p>Cualquier tipo de contacto sexual sin consentimiento es legalmente considerado una violación o acoso sexual. Y no solo esto, ser presionado para ser parte de una situación sexual no deseada puede originar daños emocionales. No olvides que no es suficiente asumir que alguien quiere tener sexo tanto como tú, realmente debes preguntar.</p>
				<h3>¿Cómo doy y recibo consentimiento?</h3>
				<p>La única forma de saber si alguien te está dando su consentimiento es si te lo dicen claramente. Algunas veces puede que a tu pareja le gusta algo, pero en su interior no es así. Una de las maneras de determinar si alguien esta incómodo con una situación (especialmente una sexual) es simplemente preguntando, como en estos ejemplos:</p>
				<h4 class="text-muted"><li>"¿Te sientes bien con esto?"</li></h4>
				<h4 class="text-muted"><li>"¿Te gusta que te haga eso?"</li></h4>
				<h4 class="text-muted"><li>"¿Quieres que me detenga?"</li></h4>
				<h4 class="text-muted"><li>"¿Quieres que continúe?"</li></h4>
				<p>Las expresiones faciales y el lenguaje corporal es una forma muy común de expresar disgusto por cierta práctica o situación, Si quieres expresar que NO quieres algo, puede expresarlo así:</p>
				<h4 class="text-muted"><li>“¿Podemos detenernos?”</li></h4>
				<h4 class="text-muted"><li>“No estoy list@ para esto”</li></h4>
				<h4 class="text-muted"><li>“¿Podemos bajar el ritmo por un segundo?”</li></h4>
				<h4 class="text-muted"><li>“Puedes tocarme lo que quieras por acá, pero no por acá”</li></h4>
				<p>¡Recuerda!, siempre tienes derecho a decir “No”, y siempre tendrás la libertad de cambiar de opinión sin importar que tan lejos hayan llegado las cosas. No importa que sea tu novi@ o que ya hayan tenido sexo en el pasado, tu no le debes nada a nadie y nadie te debe nada. Cada quien es dueño de su cuerpo.</p>
				<p>Lo importante es tomarte tu tiempo, asegurarse de que ambos están cómodos con la situación y así podrán garantizar sexo seguro, mucho más placentero y satisfactorio.</p>
				<h3>Cuando hay alcohol y drogas involucradas…</h3>
				<p>Las drogas y el alcohol pueden afectar la habilidad de las personas de tomar decisiones, incluyendo si quieren o no compartir una experiencia sexual con alguien. Una pareja intoxicada no puede dar consentimiento legal referente a una actividad sexual. Según el Centro de Salud de la NC State University, 84% de las violaciones comunes involucran alcohol o personas altamente intoxicadas.</p>  
				<p>¿Y qué hay cuando ambas partes están intoxicadas? Es simple, el sexo es algo activo y requiere de una acción intencional, así que requiere por lo menos de una de las partes para que funcione. Recuerda, el sexo no es algo que le sucede a alguien, es algo que le sucede a ambos.</p> 
				<p>Es importante cuidar de personas que se encuentran en un estado vulnerable, pues el resultado final es que en ese momento no pueden entender las consecuencias físicas y emocionales de sus acciones.</p> 
				<p>Siempre debes tener una conversación abierta respecto al sexo incluyendo sus emocionantes variaciones, y no debes presionar a alguien que no se sienta listo. La clave del sexo placentero es que ambos se sientan entusiastas en relación a lo que quieren probar. Probar cosas nuevas con alguien es apasionante y sensual, siempre que sea el momento y la persona con la que te sientas cómodo o cómoda haciéndolo.</p>
