						<h3>¿“Argumentos” contra la adopción igualitaria?</h3>
						<p>Que es que la homosexualidad es contra la naturaleza. Ah ya. Me imagino que la misma naturaleza a la que pertenece la ducha en la que se bañaron esta mañana o el computador desde el que escriben todas esas cosas incomprensibles para mí. O es esa naturaleza en la que todos los humanos debemos basar nuestro comportamiento, en la que los leones se comen a los leoncitos para que la leona quede otra vez lista para aparearse. ¿No? Ah, ya! Contra la naturaleza de la que hablan Dios y la moral...Claro, ese Dios y esa moral que indefectiblemente todos compartimos y que a los que no la compartan hay que imponérselas por todos los medios posibles, porque por una razón todavía desconocida para la raza humana, es un Dios y una moral superiores a cualquiera otros.</p>
						<img src="../_img_articulos/family_md.jpg" />
						<p>Que es que Colombia “no está preparada” para esto. Que qué tal que los hijos de familias homoparentales les hagan bullying en el colegio, por ejemplo. Heterosexuales miopes del mundo, favor de no reproducirse porque a sus hijos gafufos puede que les hagan bullying! Por favor. Yo no me acuerdo cuándo, cómo, dónde ni quién me explicó que algunas personas tienen papás divorciados. Lo que sí me acuerdo es que toda la vida en el colegio crecí con muchas personas de padres divorciados y solo me vine a enterar que en algún momento de la historia de la humanidad eso fue un tema tabú cuando para el prom algunas personas reservaron dos mesas, una para cada uno de sus padres divorciados, y unas señoras de la Asociación de Padres de Familia hicieron cara de fuchi. Seguro, para ese entonces, cuando a todos los peladitos jamás nos llamó siquiera la atención que algunos de nuestros compañeros tuvieran papás separados, ellas todavía creían que no estábamos preparados para eso. O hasta de pronto le decían a sus hijos que le hicieran bullying a los hijos de esos inmorales.</p> 
						
						<p>Y mi favorito: que la familia es una institución sagrada y tradicional que hay que proteger. ¿Cuál familia? ¿Una como la mía con un papá y una mamá y una hermana y un hermano? ¿Y qué pasa con la madre soltera que con la ayuda de su hermana sacó adelante a una hija?¿Esa no es familia? ¿O esa no la protegemos? ¿O qué me dicen de la abuela que, solita, se hizo cargo de su nieto? ¿Y el señor que enviudó con hijos menores de edad? ¿Le decimos que como en su casa no hay una mamá entonces que esa familia no es de las sagradas y tradicionales?</p>
						
						<h3>Señor Magistrado: decida usted en Derecho y no en prejuicio</h3>
						<p>Es absurdo pretender que un juez constitucional dicte quién puede y quién no configurar una familia. Y por eso me atrevo a decir que yo no soy la única que no está entendiendo algo. Al parecer algunos no entienden qué es y para qué sirve un juez constitucional. Como abogada que NO soy, me permito dar mi propia definición de juez constitucional. Algo así como “Juez constitucional para dummies”, o para los perezosos que no se han dignado dejar la Biblia a un lado y abrir la Constitución.</p>
						
						<p>Un juez constitucional es aquel que, con base en el Derecho y la Constitución, decide la resolución frente a un problema jurídico. Y para resolver cualquier problema jurídico los jueces cuentan con lo que, figurativamente, llamaré “evidencia”. En este caso, por ejemplo, la evidencia número 1 está contenida en los estudios sociológicos, científicos, psicológicos y demás ‘icos’ que presentaron, por ejemplo, las intervenciones ciudadanas y algunas instituciones del Estado frente a la Corte Constitucional (entre esas, el Bienestar Familiar, la Fiscalía, la Defensoría del Pueblo, Profamilia y, felizmente, una enorme mayoría de las Universidades más respetables y respetadas del país). Esas que, con abrumadora evidencia científica han probado que los padres homosexuales son igualiticos a los heterosexuales. La evidencia número 2 es la propia Constitución que dice que los derechos de los niños priman sobre los derechos de cualquier otra persona. Y aquí viene algo más que creo que en muchos casos no ha quedado claro. Y es que acá no se está discutiendo si alguien tiene un “derecho a adoptar” sino que están en juego los derechos más fundamentales de los niños, esos que se garantizan cuando ellos crecen en una familia que los ama, los cuida y los educa. La discusión, entonces, es si las parejas homosexuales pueden brindarle a esos niños esa familia idónea gracias a la cual ellos pueden crecer con un montón de derechos que se derivan de ahí. Y los estudios ya han probado que sí (y que cuando no eso no tiene que ver con la orientación sexual de los papás).</p> 
						
						<p>Conclusión, si los señores Magistrados están o no personalmente de acuerdo con que las parejas homosexuales adopten eso solo le importa a ellos y a sus familias. Y si mucha gente en Colombia no está de acuerdo o no se siente preparada para esta situación pues, como nación, nos importa muchísimo menos. Lo importante aquí es que los Magistrados cumplan su deber y vean las evidencias que se les presentan, “iluminados” por el Derecho, la Constitución y la democracia. Y que si después quieren mandar a sus hijos a que le hagan bullying a los hijos de los homosexuales, pues que lo hagan, que yo estoy segura que, en ese caso, los niños de Colombia serán los más desobedientes del mundo.</p> 
						
